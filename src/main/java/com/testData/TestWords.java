    public static int rnd(int min, int max) {
        max -= min;
        return (int) (Math.random() * ++max) + min;
    }

    public  static  Word GetRandom(){
        int i = rnd(0, Get().size() - 1);
        return Get().get(i);
    }

    public static int countItemInPage = 60;

    public static int countPage(){
        int countPage = Get().size() / countItemInPage;
        int lastPage = Get().size() % countPage;
        if(lastPage > 0){
            countPage += 1;
        }
        return countPage;
    }

    public  static ArrayList<Word> GetDictionaryPage(int index){
        int countPage = countPage();
        if (index > countPage){
            index = countPage;
        }
        int start = (index) * countItemInPage;
        int end = (index+1) * countItemInPage;
        if (end >= Get().size()){
            end = Get().size()-1;
        }
        return new ArrayList<Word>(Get().subList(start, end));
    }
